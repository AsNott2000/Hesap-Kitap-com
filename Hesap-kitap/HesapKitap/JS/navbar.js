 function dropdown_matematik(){
     document.querySelector('.matematik').style.cssText =
     'display:block; position:absolute; background-color:white; '

     document.getElementById('matematik').style.cssText = 
     ' background: #fff;color: #818181;border-radius: 0rem;'+
     'border-top-right-radius:.5rem; border-top-left-radius:.5rem;' 
}

function dropdownout_matematik(){
    document.querySelector('.matematik').style.cssText =
    'display:none;'

    document.getElementById('matematik').style.cssText = 
    ' background: transparent;color: #fff; ' 
}


